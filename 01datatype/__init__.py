# int类型

# 计算：+ - * / // %
a = 1 + 2
print('int类型相加：1 + 2=',a)

a = 3 - 1
print('int类型相减：3 - 1=',a)

a = 3 * 4
print('int类型相乘：3 * 4=',a)

a = 9 / 2
print('int类型除数：9 / 2=',a)

a = 9 // 2
print('int类型取整除：9 // 2=',a)

a = 9 % 2
print('int类型取余数: 9%2=',a)


# complex类型 复数 但复数并不是i，而是j
x=3+2j
y=4-3j
print('复数相乘',x*y)